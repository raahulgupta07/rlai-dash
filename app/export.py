@router.post("/pptx")
async def export_pptx(request: Request):
    """Generate a PPTX from dashboard widgets or slides data."""
    _get_user(request)
    body = await request.json()
    title = _re.sub(r'[^\w\s\-.]', '', body.get("title", "Dash Report"))[:100] or 'export'
    slides = body.get("slides", [])

    if not slides:
        raise HTTPException(400, "Slides required")

    try:
        from pptx import Presentation
        from pptx.util import Inches, Pt
    except ImportError:
        raise HTTPException(500, "python-pptx not installed")

    prs = Presentation()
    prs.slide_width = Inches(13.333)
    prs.slide_height = Inches(7.5)

    # Title slide
    title_layout = prs.slide_layouts[0]
    slide = prs.slides.add_slide(title_layout)
    slide.shapes.title.text = title
    if slide.placeholders[1]:
        slide.placeholders[1].text = "Generated by Dash"

    # Content slides
    blank_layout = prs.slide_layouts[len(prs.slide_layouts) - 1]  # truly blank
    for s in slides:
        slide = prs.slides.add_slide(blank_layout)
        # Remove all default placeholders
        for ph in list(slide.placeholders):
            sp = ph._element
            sp.getparent().remove(sp)
        # Title
        from pptx.util import Emu
        txBox = slide.shapes.add_textbox(Inches(0.5), Inches(0.3), Inches(12), Inches(0.8))
        tf = txBox.text_frame
        p = tf.paragraphs[0]
        p.text = s.get("title", "")
        p.font.size = Pt(24)
        p.font.bold = True

        # Content
        content = s.get("content", "")
        if content:
            txBox2 = slide.shapes.add_textbox(Inches(0.5), Inches(1.3), Inches(12), Inches(5.5))
            tf2 = txBox2.text_frame
            tf2.word_wrap = True
            # Split content into lines
            for line in content.split("\n"):
                p2 = tf2.add_paragraph()
                p2.text = line
                p2.font.size = Pt(14)

        # Table data
        headers = s.get("headers", [])
        rows = s.get("rows", [])
        if headers and rows:
            num_cols = len(headers)
            num_rows = min(len(rows), 15) + 1  # header + data rows
            left = Inches(0.5)
            top = Inches(1.5) if not content else Inches(4.0)
            width = Inches(12)
            height = Inches(0.3 * num_rows)

            table_shape = slide.shapes.add_table(num_rows, num_cols, left, top, width, height)
            table = table_shape.table

            for ci, h in enumerate(headers):
                cell = table.cell(0, ci)
                cell.text = h
                for paragraph in cell.text_frame.paragraphs:
                    paragraph.font.size = Pt(10)
                    paragraph.font.bold = True

            for ri, row in enumerate(rows[:15]):
                for ci, val in enumerate(row):
                    if ci < num_cols:
                        cell = table.cell(ri + 1, ci)
                        cell.text = str(val)
                        for paragraph in cell.text_frame.paragraphs:
                            paragraph.font.size = Pt(9)

    buf = io.BytesIO()
    prs.save(buf)
    buf.seek(0)
    return StreamingResponse(buf, media_type="application/vnd.openxmlformats-officedocument.presentationml.presentation",
                             headers={"Content-Disposition": f'attachment; filename="{title}.pptx"'})


@router.post("/slides-agent")
async def slides_agent(request: Request):
    """Slide Agent: Think → Structure → Generate slides from chat."""
    _get_user(request)
    body = await request.json()
    messages = body.get("messages", [])
    title = _re.sub(r'[^\w\s\-.]', '', body.get("title", "Analysis Report"))[:100] or 'export'
    agent_name = body.get("agent_name", "Dash Agent")

    if not messages:
        raise HTTPException(400, "Messages required")

    convo = "\n\n".join(
        f"{'Q' if m.get('role') == 'user' else 'A'}: {m.get('content', '')[:1500]}"
        for m in messages[-20:]
    )

    from os import getenv
    import httpx, json
    from dash.settings import _repair_json

    api_key = getenv("OPENROUTER_API_KEY", "")
    thinking = {}
    slides = []

    if api_key:
        try:
            # ── LLM Call 1: THINK ──────────────────────────────────────
            think_prompt = f"""You are a McKinsey presentation strategist. Analyze this data analysis conversation and plan a compelling presentation.

CONVERSATION:
{convo[:5000]}

RULES:
1. Find the ONE key insight that would surprise the audience
2. Structure as: Situation → Complication → Resolution
3. Every slide title must be a FULL SENTENCE that states the insight (not a label)
   BAD: "Revenue by Channel"
   GOOD: "Ocean and Citymart drive 69% of revenue, but Citymart grows 2x faster"
4. Plan 6-8 slides

Return JSON:
{{
  "narrative": "The main story arc in one sentence",
  "key_insight": "The one surprising finding that changes perspective",
  "audience_action": "The specific action the audience should take",
  "slide_plan": [
    {{"title": "Full sentence insight", "layout": "cover|kpi|exhibit|data|comparison|trend|recommendations", "data_needed": "what numbers to extract"}}
  ]
}}"""

            think_resp = httpx.post(
                "https://openrouter.ai/api/v1/chat/completions",
                headers={"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"},
                json={
                    "model": "openai/gpt-5.4-mini",
                    "messages": [{"role": "user", "content": think_prompt}],
                    "max_tokens": 2000,
                    "temperature": 0.3,
                },
                timeout=30,
            )
            think_raw = think_resp.json().get("choices", [{}])[0].get("message", {}).get("content", "")
            think_raw = _repair_json(think_raw)
            thinking = json.loads(think_raw)

            # ── LLM Call 2: GENERATE ───────────────────────────────────
            gen_prompt = f"""Generate McKinsey-style presentation slides. Every slide must follow these rules:

MCKINSKEY SLIDE RULES:
1. Title = Full sentence insight (NOT a label)
2. Every slide has an "action_line" starting with → that tells what to DO
3. Chart slides MUST have real numeric data extracted from the conversation
4. Table slides MUST have real data rows from the conversation
5. KPI slides MUST have 2-4 big numbers with labels and change indicators

PLAN: {json.dumps(thinking)}
ORIGINAL DATA: {convo[:4000]}

For each slide, generate this JSON:
{{
  "title": "Full sentence insight (the action title)",
  "layout": "cover|kpi|exhibit|data|comparison|recommendations",
  "topic": "Short topic label for header bar (2-3 words)",
  "bullets": ["numbered takeaway 1", "numbered takeaway 2", "numbered takeaway 3"],
  "chart": {{"type": "bar|line|pie|horizontal_bar", "labels": ["real label 1", "real label 2"], "values": [0, 0], "title": "Exhibit title"}},
  "table": {{"headers": ["Col1", "Col2", "Col3"], "rows": [["real data", "real data", "real data"]]}},
  "kpis": [{{"value": "2.60T", "label": "Total Revenue", "change": "+12%"}}, {{"value": "4.03B", "label": "Avg Daily", "change": "+5%"}}],
  "action_line": "→ Specific action the audience should take based on this slide",
  "comparison": {{"left": {{"title": "A", "items": ["metric: value"]}}, "right": {{"title": "B", "items": ["metric: value"]}}}}
}}

CRITICAL:
- chart, table, kpis, comparison are OPTIONAL — include only when layout matches
- Extract REAL numbers from the conversation — DO NOT use placeholder data
- First slide must be layout "cover"
- Last slide must be layout "recommendations" with 3-4 priority cards
- Include at least 2 slides with chart data
- Include at least 1 slide with a table

Return ONLY a valid JSON array of 6-8 slide objects."""

            gen_resp = httpx.post(
                "https://openrouter.ai/api/v1/chat/completions",
                headers={"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"},
                json={
                    "model": "openai/gpt-5.4-mini",
                    "messages": [{"role": "user", "content": gen_prompt}],
                    "max_tokens": 4000,
                    "temperature": 0.2,
                },
                timeout=30,
            )
            gen_raw = gen_resp.json().get("choices", [{}])[0].get("message", {}).get("content", "")
            gen_raw = _repair_json(gen_raw)
            slides = json.loads(gen_raw)

        except Exception:
            pass

    # Fallback: basic slides from chat messages
    if not slides:
        slides = [
            {"title": title, "layout": "title", "bullets": [f"Generated by {agent_name}"], "so_what": "Overview"},
        ]
        for m in messages:
            if m.get("role") == "assistant" and m.get("content"):
                slides.append({
                    "title": "Analysis",
                    "layout": "bullets",
                    "bullets": [m["content"][:300]],
                    "so_what": "Key finding from conversation",
                })
        if not thinking:
            thinking = {"narrative": title, "key_insight": "See slides", "audience_action": "Review findings"}

    return {
        "status": "ok",
        "thinking": {
            "narrative": thinking.get("narrative", ""),
            "key_insight": thinking.get("key_insight", ""),
            "audience_action": thinking.get("audience_action", ""),
        },
        "slides": slides,
    }


