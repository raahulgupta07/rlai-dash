@router.post("/report-from-chat")
async def generate_report_from_chat(request: Request):
    """Generate a structured report from a chat session."""
    _get_user(request)
    body = await request.json()
    messages = body.get("messages", [])
    title = _re.sub(r'[^\w\s\-.]', '', body.get("title", "Analysis Report"))[:100] or 'export'

    if not messages:
        raise HTTPException(400, "Messages required")

    # Build conversation text
    convo = "\n\n".join(f"{'Q' if m.get('role') == 'user' else 'A'}: {m.get('content', '')}" for m in messages)

    from os import getenv
    import httpx
    api_key = getenv("OPENROUTER_API_KEY", "")

    report_content = f"# {title}\n\n"
    if api_key:
        try:
            prompt = f"""Convert this data analysis conversation into a structured report.

CONVERSATION:
{convo[:3000]}

Generate a professional report with these sections:
1. Executive Summary (2-3 sentences)
2. Key Findings (bullet points)
3. Data Analysis (details with any tables/numbers mentioned)
4. Recommendations (if applicable)

Write in markdown format."""

            resp = httpx.post(
                "https://openrouter.ai/api/v1/chat/completions",
                headers={"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"},
                json={"model": "openai/gpt-5.4-mini", "messages": [{"role": "user", "content": prompt}], "max_tokens": 2000, "temperature": 0.2},
                timeout=20,
            )
            result = resp.json()
            report_content = result.get("choices", [{}])[0].get("message", {}).get("content", report_content)
        except Exception:
            # Fallback: just concatenate the conversation
            report_content += convo
    else:
        report_content += convo

    try:
        import markdown as md
        from weasyprint import HTML
        html_body = md.markdown(report_content, extensions=["tables", "fenced_code"])
        full_html = _build_html(title, html_body, is_raw_html=True)
        pdf_bytes = HTML(string=full_html).write_pdf()
        buf = io.BytesIO(pdf_bytes)
        return StreamingResponse(buf, media_type="application/pdf",
                                 headers={"Content-Disposition": f'attachment; filename="{title}.pdf"'})
    except ImportError:
        html_content = _build_html(title, report_content)
        buf = io.BytesIO(html_content.encode("utf-8"))
        return StreamingResponse(buf, media_type="text/html",
                                 headers={"Content-Disposition": f'attachment; filename="{title}.html"'})


