def _get_user(request: Request) -> dict:
    user = getattr(getattr(request, 'state', None), 'user', None)
    if not user:
        raise HTTPException(401, "Not authenticated")
    return user


@router.post("/pdf")
async def export_pdf(request: Request):
    """Generate a PDF from markdown/HTML content."""
    _get_user(request)
    body = await request.json()
    content = body.get("content", "")
    title = _re.sub(r'[^\w\s\-.]', '', body.get("title", "Dash Export"))[:100] or 'export'

    if not content:
        raise HTTPException(400, "Content required")

    try:
        import markdown as md
        from weasyprint import HTML
    except ImportError:
        # Fallback: return HTML as downloadable file
        html_content = _build_html(title, content)
        buf = io.BytesIO(html_content.encode("utf-8"))
        return StreamingResponse(buf, media_type="text/html",
                                 headers={"Content-Disposition": f'attachment; filename="{title}.html"'})

    html_body = md.markdown(content, extensions=["tables", "fenced_code"])
    full_html = _build_html(title, html_body, is_raw_html=True)

    pdf_bytes = HTML(string=full_html).write_pdf()
    buf = io.BytesIO(pdf_bytes)
    return StreamingResponse(buf, media_type="application/pdf",
                             headers={"Content-Disposition": f'attachment; filename="{title}.pdf"'})


@router.post("/pptx")
async def export_pptx(request: Request):
    """Generate a PPTX from dashboard widgets or slides data."""
    _get_user(request)
    body = await request.json()
    title = _re.sub(r'[^\w\s\-.]', '', body.get("title", "Dash Report"))[:100] or 'export'
    slides = body.get("slides", [])

    if not slides:
        raise HTTPException(400, "Slides required")

    try:
        from pptx import Presentation
        from pptx.util import Inches, Pt
        from pptx.dml.color import RGBColor
    except ImportError:
        raise HTTPException(500, "python-pptx not installed")

    # Get theme from request body
    theme_name = body.get("theme", "ocean_gradient")
    theme = DESIGN_THEMES.get(theme_name, DESIGN_THEMES["ocean_gradient"])

    def _hex_to_rgb(hex_str):
        return RGBColor(int(hex_str[:2], 16), int(hex_str[2:4], 16), int(hex_str[4:6], 16))

    prs = Presentation()
    prs.slide_width = Inches(13.333)
    prs.slide_height = Inches(7.5)

    # Title slide with theme dark background
    blank_layout = prs.slide_layouts[len(prs.slide_layouts) - 1]
    slide = prs.slides.add_slide(blank_layout)
    for ph in list(slide.placeholders):
        sp = ph._element
        sp.getparent().remove(sp)
    # Dark background for title
    bg = slide.background
    fill = bg.fill
    fill.solid()
    fill.fore_color.rgb = _hex_to_rgb(theme["bg_dark"])
    # Title text
    txBox = slide.shapes.add_textbox(Inches(1.5), Inches(2.0), Inches(10), Inches(2.0))
    txBox.text_frame.word_wrap = True
    p = txBox.text_frame.paragraphs[0]
    p.text = title
    p.font.size = Pt(36)
    p.font.bold = True
    p.font.color.rgb = _hex_to_rgb(theme["text_light"])
    p.font.name = theme["header_font"]
    from pptx.enum.text import PP_ALIGN
    p.alignment = PP_ALIGN.CENTER
    # Subtitle
    txBox2 = slide.shapes.add_textbox(Inches(2), Inches(4.5), Inches(9), Inches(0.6))
    p2 = txBox2.text_frame.paragraphs[0]
    p2.text = "Generated by Dash"
    p2.font.size = Pt(16)
    p2.font.color.rgb = _hex_to_rgb(theme["secondary"])
    p2.font.name = theme["body_font"]
    p2.alignment = PP_ALIGN.CENTER

    # Content slides
    for s in slides:
        slide = prs.slides.add_slide(blank_layout)
        # Remove all default placeholders
        for ph in list(slide.placeholders):
            sp = ph._element
            sp.getparent().remove(sp)

        # Apply theme background based on slide bg field
        bg_type = s.get("bg", "light")
        slide_bg = slide.background
        slide_fill = slide_bg.fill
        slide_fill.solid()
        if bg_type == "dark":
            slide_fill.fore_color.rgb = _hex_to_rgb(theme["bg_dark"])
            text_color = _hex_to_rgb(theme["text_light"])
        else:
            slide_fill.fore_color.rgb = _hex_to_rgb(theme["bg_light"])
            text_color = _hex_to_rgb(theme["text_dark"])

        # Title
        from pptx.util import Emu
        txBox = slide.shapes.add_textbox(Inches(0.5), Inches(0.3), Inches(12), Inches(0.8))
        tf = txBox.text_frame
        tf.word_wrap = True
        p = tf.paragraphs[0]
        p.text = s.get("title", "")
        p.font.size = Pt(36)
        p.font.bold = True
        p.font.color.rgb = text_color
        p.font.name = theme["header_font"]

        # Content
        content = s.get("content", "")
        if content:
            txBox2 = slide.shapes.add_textbox(Inches(0.5), Inches(1.3), Inches(12), Inches(5.5))
            tf2 = txBox2.text_frame
            tf2.word_wrap = True
            # Split content into lines
            for line in content.split("\n"):
                p2 = tf2.add_paragraph()
                p2.text = line
                p2.font.size = Pt(14)
                p2.font.color.rgb = text_color
                p2.font.name = theme["body_font"]

        # Table data
        headers = s.get("headers", [])
        rows = s.get("rows", [])
        if headers and rows:
            num_cols = len(headers)
            num_rows = min(len(rows), 15) + 1  # header + data rows
            left = Inches(0.5)
            top = Inches(1.5) if not content else Inches(4.0)
            width = Inches(12)
            height = Inches(0.3 * num_rows)

            table_shape = slide.shapes.add_table(num_rows, num_cols, left, top, width, height)
            table = table_shape.table

            for ci, h in enumerate(headers):
                cell = table.cell(0, ci)
                cell.text = h
                # Theme primary color background for header cells
                try:
                    cell.fill.solid()
                    cell.fill.fore_color.rgb = _hex_to_rgb(theme["primary"])
                except Exception:
                    pass
                for paragraph in cell.text_frame.paragraphs:
                    paragraph.font.size = Pt(10)
                    paragraph.font.bold = True
                    paragraph.font.color.rgb = _hex_to_rgb(theme["text_light"])
                    paragraph.font.name = theme["body_font"]

            for ri, row in enumerate(rows[:15]):
                for ci, val in enumerate(row):
                    if ci < num_cols:
                        cell = table.cell(ri + 1, ci)
                        cell.text = str(val)
                        for paragraph in cell.text_frame.paragraphs:
                            paragraph.font.size = Pt(9)
                            paragraph.font.name = theme["body_font"]

    buf = io.BytesIO()
    prs.save(buf)
    buf.seek(0)
    return StreamingResponse(buf, media_type="application/vnd.openxmlformats-officedocument.presentationml.presentation",
                             headers={"Content-Disposition": f'attachment; filename="{title}.pptx"'})


