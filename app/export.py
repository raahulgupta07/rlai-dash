@router.post("/pptx")
async def export_pptx(request: Request):
    """Generate a PPTX from dashboard widgets or slides data."""
    _get_user(request)
    body = await request.json()
    title = _re.sub(r'[^\w\s\-.]', '', body.get("title", "Dash Report"))[:100] or 'export'
    slides = body.get("slides", [])

    if not slides:
        raise HTTPException(400, "Slides required")

    try:
        from pptx import Presentation
        from pptx.util import Inches, Pt
    except ImportError:
        raise HTTPException(500, "python-pptx not installed")

    prs = Presentation()
    prs.slide_width = Inches(13.333)
    prs.slide_height = Inches(7.5)

    # Title slide
    title_layout = prs.slide_layouts[0]
    slide = prs.slides.add_slide(title_layout)
    slide.shapes.title.text = title
    if slide.placeholders[1]:
        slide.placeholders[1].text = "Generated by Dash"

    # Content slides
    blank_layout = prs.slide_layouts[5]  # blank
    for s in slides:
        slide = prs.slides.add_slide(blank_layout)
        # Title
        from pptx.util import Emu
        txBox = slide.shapes.add_textbox(Inches(0.5), Inches(0.3), Inches(12), Inches(0.8))
        tf = txBox.text_frame
        p = tf.paragraphs[0]
        p.text = s.get("title", "")
        p.font.size = Pt(24)
        p.font.bold = True

        # Content
        content = s.get("content", "")
        if content:
            txBox2 = slide.shapes.add_textbox(Inches(0.5), Inches(1.3), Inches(12), Inches(5.5))
            tf2 = txBox2.text_frame
            tf2.word_wrap = True
            # Split content into lines
            for line in content.split("\n"):
                p2 = tf2.add_paragraph()
                p2.text = line
                p2.font.size = Pt(14)

        # Table data
        headers = s.get("headers", [])
        rows = s.get("rows", [])
        if headers and rows:
            num_cols = len(headers)
            num_rows = min(len(rows), 15) + 1  # header + data rows
            left = Inches(0.5)
            top = Inches(1.5) if not content else Inches(4.0)
            width = Inches(12)
            height = Inches(0.3 * num_rows)

            table_shape = slide.shapes.add_table(num_rows, num_cols, left, top, width, height)
            table = table_shape.table

            for ci, h in enumerate(headers):
                cell = table.cell(0, ci)
                cell.text = h
                for paragraph in cell.text_frame.paragraphs:
                    paragraph.font.size = Pt(10)
                    paragraph.font.bold = True

            for ri, row in enumerate(rows[:15]):
                for ci, val in enumerate(row):
                    if ci < num_cols:
                        cell = table.cell(ri + 1, ci)
                        cell.text = str(val)
                        for paragraph in cell.text_frame.paragraphs:
                            paragraph.font.size = Pt(9)

    buf = io.BytesIO()
    prs.save(buf)
    buf.seek(0)
    return StreamingResponse(buf, media_type="application/vnd.openxmlformats-officedocument.presentationml.presentation",
                             headers={"Content-Disposition": f'attachment; filename="{title}.pptx"'})


@router.post("/slides-agent")
async def slides_agent(request: Request):
    """Slide Agent: Think → Structure → Generate slides from chat."""
    _get_user(request)
    body = await request.json()
    messages = body.get("messages", [])
    title = _re.sub(r'[^\w\s\-.]', '', body.get("title", "Analysis Report"))[:100] or 'export'
    agent_name = body.get("agent_name", "Dash Agent")

    if not messages:
        raise HTTPException(400, "Messages required")

    convo = "\n\n".join(
        f"{'Q' if m.get('role') == 'user' else 'A'}: {m.get('content', '')[:1500]}"
        for m in messages[-20:]
    )

    from os import getenv
    import httpx, json
    from dash.settings import _repair_json

    api_key = getenv("OPENROUTER_API_KEY", "")
    thinking = {}
    slides = []

    if api_key:
        try:
            # ── LLM Call 1: THINK ──────────────────────────────────────
            think_prompt = f"""You are a McKinsey presentation strategist. Analyze this conversation and plan a presentation.

CONVERSATION:
{convo[:5000]}

Return JSON:
{{
  "narrative": "The main story this data tells",
  "key_insight": "The one surprising finding",
  "audience_action": "What the audience should do after seeing this",
  "slide_plan": [
    {{"title": "...", "layout": "title|kpi|chart|table|comparison|bullets|summary", "purpose": "why this slide"}}
  ]
}}"""

            think_resp = httpx.post(
                "https://openrouter.ai/api/v1/chat/completions",
                headers={"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"},
                json={
                    "model": "openai/gpt-5.4-mini",
                    "messages": [{"role": "user", "content": think_prompt}],
                    "max_tokens": 2000,
                    "temperature": 0.3,
                },
                timeout=30,
            )
            think_raw = think_resp.json().get("choices", [{}])[0].get("message", {}).get("content", "")
            think_raw = _repair_json(think_raw)
            thinking = json.loads(think_raw)

            # ── LLM Call 2: GENERATE ───────────────────────────────────
            gen_prompt = f"""Generate presentation slides based on this plan.

PLAN: {json.dumps(thinking)}
ORIGINAL DATA: {convo[:4000]}

For each slide in the plan, generate:
{{
  "title": "...",
  "layout": "title|kpi|chart|table|comparison|bullets|summary",
  "bullets": ["point 1", "point 2"],
  "chart": {{"type": "bar|line|pie", "labels": [...], "values": [...], "title": "..."}},
  "table": {{"headers": [...], "rows": [...]}},
  "kpi": {{"label": "...", "value": "...", "change": "+12%"}},
  "so_what": "The key takeaway from this slide"
}}

chart, table, and kpi are optional — include only when relevant to the slide layout.
Extract REAL numbers from the conversation. Every slide must have a 'so_what'.
Return ONLY a valid JSON array of slide objects."""

            gen_resp = httpx.post(
                "https://openrouter.ai/api/v1/chat/completions",
                headers={"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"},
                json={
                    "model": "openai/gpt-5.4-mini",
                    "messages": [{"role": "user", "content": gen_prompt}],
                    "max_tokens": 3000,
                    "temperature": 0.2,
                },
                timeout=30,
            )
            gen_raw = gen_resp.json().get("choices", [{}])[0].get("message", {}).get("content", "")
            gen_raw = _repair_json(gen_raw)
            slides = json.loads(gen_raw)

        except Exception:
            pass

    # Fallback: basic slides from chat messages
    if not slides:
        slides = [
            {"title": title, "layout": "title", "bullets": [f"Generated by {agent_name}"], "so_what": "Overview"},
        ]
        for m in messages:
            if m.get("role") == "assistant" and m.get("content"):
                slides.append({
                    "title": "Analysis",
                    "layout": "bullets",
                    "bullets": [m["content"][:300]],
                    "so_what": "Key finding from conversation",
                })
        if not thinking:
            thinking = {"narrative": title, "key_insight": "See slides", "audience_action": "Review findings"}

    return {
        "status": "ok",
        "thinking": {
            "narrative": thinking.get("narrative", ""),
            "key_insight": thinking.get("key_insight", ""),
            "audience_action": thinking.get("audience_action", ""),
        },
        "slides": slides,
    }


@router.post("/pptx-from-chat")
async def export_pptx_from_chat(request: Request):
    """Generate a McKinsey-style PPTX from chat messages (workflow results)."""
    _get_user(request)
    body = await request.json()
    messages = body.get("messages", [])
    title = _re.sub(r'[^\w\s\-.]', '', body.get("title", "Analysis Report"))[:100] or 'export'

    if not messages:
        raise HTTPException(400, "Messages required")

    # Build conversation for LLM
    convo = "\n\n".join(f"{'Q' if m.get('role') == 'user' else 'A'}: {m.get('content', '')[:1500]}" for m in messages[-20:])

    from os import getenv
    import httpx
    api_key = getenv("OPENROUTER_API_KEY", "")

    # LLM: convert chat to slide structure
    slides_data = []
    if api_key:
        try:
            prompt = f"""Convert this data analysis conversation into presentation slides.

CONVERSATION:
{convo[:4000]}

Generate 5-8 slides as a JSON array. Each slide has:
- "title": short headline (max 10 words)
- "content": 3-5 bullet points (key findings, insights, numbers)
- "slide_type": one of "title", "findings", "data", "recommendation", "summary"

Rules:
- First slide: title slide with report name
- Include specific numbers/percentages from the data
- Last slide: recommendations or next steps
- Keep bullets concise (under 15 words each)
- Think like McKinsey — insight-driven, action-oriented

Return ONLY valid JSON array, no markdown."""

            resp = httpx.post(
                "https://openrouter.ai/api/v1/chat/completions",
                headers={"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"},
                json={"model": "openai/gpt-5.4-mini", "messages": [{"role": "user", "content": prompt}], "max_tokens": 2000, "temperature": 0.3},
                timeout=30,
            )
            import json
            result = resp.json()
            content = result.get("choices", [{}])[0].get("message", {}).get("content", "")
            # Clean JSON
            content = content.strip()
            if content.startswith("```"):
                content = content.split("\n", 1)[-1]
            if content.endswith("```"):
                content = content[:-3]
            content = content.strip()
            if content.lower().startswith("json"):
                content = content[4:].strip()
            slides_data = json.loads(content)
        except Exception:
            pass

    # Fallback: one slide per message
    if not slides_data:
        slides_data = [{"title": title, "content": "Generated by Dash", "slide_type": "title"}]
        for m in messages:
            if m.get("role") == "assistant" and m.get("content"):
                text = m["content"][:500]
                slides_data.append({"title": "Analysis", "content": text, "slide_type": "findings"})

    # Build PPTX
    try:
        from pptx import Presentation
        from pptx.util import Inches, Pt, Emu
        from pptx.dml.color import RGBColor
        from pptx.enum.text import PP_ALIGN
    except ImportError:
        raise HTTPException(500, "python-pptx not installed")

    prs = Presentation()
    prs.slide_width = Inches(13.333)
    prs.slide_height = Inches(7.5)

    for i, s in enumerate(slides_data[:10]):
        slide_type = s.get("slide_type", "findings")
        slide = prs.slides.add_slide(prs.slide_layouts[5])  # blank

        if slide_type == "title":
            # Title slide — centered, large
            txBox = slide.shapes.add_textbox(Inches(1), Inches(2.5), Inches(11), Inches(1.5))
            tf = txBox.text_frame
            p = tf.paragraphs[0]
            p.text = s.get("title", title)
            p.font.size = Pt(40)
            p.font.bold = True
            p.alignment = PP_ALIGN.CENTER

            txBox2 = slide.shapes.add_textbox(Inches(1), Inches(4.2), Inches(11), Inches(0.8))
            tf2 = txBox2.text_frame
            p2 = tf2.paragraphs[0]
            p2.text = s.get("content", "")
            p2.font.size = Pt(18)
            p2.font.color.rgb = RGBColor(0x66, 0x66, 0x66)
            p2.alignment = PP_ALIGN.CENTER
        else:
            # Content slide — title + bullets
            # Title bar
            txBox = slide.shapes.add_textbox(Inches(0.5), Inches(0.3), Inches(12), Inches(0.8))
            tf = txBox.text_frame
            p = tf.paragraphs[0]
            p.text = s.get("title", "")
            p.font.size = Pt(28)
            p.font.bold = True
            p.font.color.rgb = RGBColor(0x1a, 0x1a, 0x1a)

            # Divider line
            from pptx.shapes.autoshape import Shape
            line = slide.shapes.add_connector(1, Inches(0.5), Inches(1.15), Inches(12.5), Inches(1.15))
            line.line.color.rgb = RGBColor(0x00, 0x96, 0x24)
            line.line.width = Pt(2)

            # Bullet content
            content = s.get("content", "")
            txBox2 = slide.shapes.add_textbox(Inches(0.7), Inches(1.4), Inches(11.5), Inches(5.5))
            tf2 = txBox2.text_frame
            tf2.word_wrap = True

            lines = content.split("\n") if "\n" in content else [content]
            for li, line_text in enumerate(lines):
                line_text = line_text.strip().lstrip("-•● ")
                if not line_text:
                    continue
                p2 = tf2.add_paragraph() if li > 0 else tf2.paragraphs[0]
                p2.text = f"  {line_text}"
                p2.font.size = Pt(16)
                p2.space_after = Pt(8)

            # Slide number
            txNum = slide.shapes.add_textbox(Inches(12.5), Inches(7.0), Inches(0.7), Inches(0.3))
            pNum = txNum.text_frame.paragraphs[0]
            pNum.text = str(i)
            pNum.font.size = Pt(9)
            pNum.font.color.rgb = RGBColor(0x99, 0x99, 0x99)
            pNum.alignment = PP_ALIGN.RIGHT

    buf = io.BytesIO()
    prs.save(buf)
    buf.seek(0)
    safe_title = _re.sub(r'[^\w\s\-.]', '', title)[:100] or 'export'
    return StreamingResponse(buf, media_type="application/vnd.openxmlformats-officedocument.presentationml.presentation",
                             headers={"Content-Disposition": f'attachment; filename="{safe_title}.pptx"'})


