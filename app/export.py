def _visual_qa_slides(pptx_bytes: bytes) -> list:
    """Convert PPTX to images and run Vision QA. Returns list of issues found."""
    try:
        import tempfile as _tmpfile
        import subprocess as _subproc
        import base64 as _b64
        import glob as _glb
        from dash.settings import training_vision_call
    except ImportError:
        return []

    issues = []
    with _tmpfile.TemporaryDirectory() as tmpdir:
        # Save PPTX
        pptx_path = f"{tmpdir}/slides.pptx"
        with open(pptx_path, "wb") as f:
            f.write(pptx_bytes)

        # Convert to PDF then images
        try:
            _subproc.run(
                ["soffice", "--headless", "--convert-to", "pdf", pptx_path, "--outdir", tmpdir],
                timeout=30, capture_output=True
            )
            pdf_path = f"{tmpdir}/slides.pdf"
            if os.path.exists(pdf_path):
                _subproc.run(
                    ["pdftoppm", "-jpeg", "-r", "150", pdf_path, f"{tmpdir}/slide"],
                    timeout=30, capture_output=True
                )

                for img_path in sorted(_glb.glob(f"{tmpdir}/slide-*.jpg"))[:8]:
                    with open(img_path, "rb") as f:
                        b64 = _b64.b64encode(f.read()).decode()

                    qa_prompt = (
                        "Inspect this presentation slide for quality issues. Look for:\n"
                        "- Overlapping text or elements\n"
                        "- Text cut off or overflowing boxes\n"
                        "- Poor contrast (light text on light bg)\n"
                        "- Uneven spacing or alignment\n"
                        "- Missing visual elements (text-only slide)\n"
                        "- Margins too small (< 0.5 inch from edges)\n"
                        'Return JSON: {"issues": ["issue 1", "issue 2"], "score": 8}\n'
                        'Score 1-10 (10=perfect). Return {"issues": [], "score": 10} if no issues.'
                    )
                    result = training_vision_call(qa_prompt, [{"b64": b64, "mime": "image/jpeg"}], task="vision")
                    if result:
                        try:
                            qa = json.loads(result)
                            issues.extend(qa.get("issues", []))
                        except Exception:
                            pass
        except Exception:
            pass

    return issues


