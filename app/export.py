@router.post("/presentations/{pres_id}/pptx")
def export_saved_pptx(pres_id: int, request: Request):
    _get_user(request)
    from sqlalchemy import create_engine as _ce, text
    from sqlalchemy.pool import NullPool
    from db import db_url
    engine = _ce(db_url, poolclass=NullPool)
    with engine.connect() as conn:
        row = conn.execute(text(
            "SELECT title, slides FROM public.dash_presentations WHERE id = :id"
        ), {"id": pres_id}).fetchone()
    if not row:
        raise HTTPException(404, "Presentation not found")

    title = row[0]
    slides = row[1] if isinstance(row[1], list) else []

    # Build PPTX using python-pptx
    try:
        from pptx import Presentation
        from pptx.util import Inches, Pt
        from pptx.dml.color import RGBColor
        from pptx.enum.text import PP_ALIGN
    except ImportError:
        raise HTTPException(500, "python-pptx not installed")

    prs = Presentation()
    prs.slide_width = Inches(13.333)
    prs.slide_height = Inches(7.5)

    # Helper: add common slide elements
    def _add_header(slide, s, i, total):
        """Add topic bar, title, divider, slide number to a content slide."""
        # Topic bar
        tb = slide.shapes.add_textbox(Inches(0.3), Inches(0.15), Inches(10), Inches(0.25))
        tp = tb.text_frame.paragraphs[0]
        tp.text = s.get("topic", "ANALYSIS").upper()
        tp.font.size = Pt(8)
        tp.font.color.rgb = RGBColor(0x88, 0x88, 0x88)
        # Slide number (top right)
        nb = slide.shapes.add_textbox(Inches(12), Inches(0.15), Inches(1), Inches(0.25))
        np2 = nb.text_frame.paragraphs[0]
        np2.text = f"{i+1} / {total}"
        np2.font.size = Pt(8)
        np2.font.color.rgb = RGBColor(0x99, 0x99, 0x99)
        np2.alignment = PP_ALIGN.RIGHT
        # Action title
        tb2 = slide.shapes.add_textbox(Inches(0.5), Inches(0.5), Inches(12), Inches(1.0))
        tb2.text_frame.word_wrap = True
        p = tb2.text_frame.paragraphs[0]
        p.text = s.get("title", "")
        p.font.size = Pt(20)
        p.font.bold = True
        # Divider
        ln = slide.shapes.add_connector(1, Inches(0.5), Inches(1.5), Inches(12.5), Inches(1.5))
        ln.line.color.rgb = RGBColor(0xD2, 0x47, 0x26)
        ln.line.width = Pt(2)

    def _add_action_line(slide, s):
        """Add action line at bottom."""
        if s.get("action_line"):
            al = slide.shapes.add_connector(1, Inches(0.5), Inches(6.3), Inches(12.5), Inches(6.3))
            al.line.color.rgb = RGBColor(0x1a, 0x1a, 0x1a)
            al.line.width = Pt(1)
            ab = slide.shapes.add_textbox(Inches(0.5), Inches(6.4), Inches(12), Inches(0.5))
            ab.text_frame.word_wrap = True
            ap = ab.text_frame.paragraphs[0]
            ap.text = s["action_line"]
            ap.font.size = Pt(11)
            ap.font.bold = True

    def _add_bullets(slide, bullets, left=0.7, top=1.7, width=11.5, height=4.0):
        """Add bullet points."""
        if not bullets:
            return
        tb = slide.shapes.add_textbox(Inches(left), Inches(top), Inches(width), Inches(height))
        tb.text_frame.word_wrap = True
        for bi, b in enumerate(bullets[:8]):
            # Strip existing numbering like "1)" or "1."
            b_clean = b.lstrip("0123456789).·- ")
            p = tb.text_frame.add_paragraph() if bi > 0 else tb.text_frame.paragraphs[0]
            p.text = b_clean
            p.font.size = Pt(13)
            p.space_after = Pt(6)

    total = len(slides[:12])
    for i, s in enumerate(slides[:12]):
        layout = s.get("layout", "bullets")
        slide = prs.slides.add_slide(prs.slide_layouts[len(prs.slide_layouts) - 1])
        for ph in list(slide.placeholders):
            sp = ph._element
            sp.getparent().remove(sp)

        if layout == "cover":
            # Centered title
            tb = slide.shapes.add_textbox(Inches(1.5), Inches(2.0), Inches(10), Inches(2.0))
            tb.text_frame.word_wrap = True
            p = tb.text_frame.paragraphs[0]
            p.text = s.get("title", title)
            p.font.size = Pt(32)
            p.font.bold = True
            p.alignment = PP_ALIGN.CENTER
            # Orange line
            ln = slide.shapes.add_connector(1, Inches(5.5), Inches(4.2), Inches(7.8), Inches(4.2))
            ln.line.color.rgb = RGBColor(0xD2, 0x47, 0x26)
            ln.line.width = Pt(3)
            # Subtitle
            if s.get("bullets"):
                tb2 = slide.shapes.add_textbox(Inches(2), Inches(4.5), Inches(9), Inches(0.6))
                p2 = tb2.text_frame.paragraphs[0]
                p2.text = s["bullets"][0]
                p2.font.size = Pt(16)
                p2.font.color.rgb = RGBColor(0x66, 0x66, 0x66)
                p2.alignment = PP_ALIGN.CENTER
            # Footer
            tb3 = slide.shapes.add_textbox(Inches(3), Inches(5.5), Inches(7), Inches(0.4))
            p3 = tb3.text_frame.paragraphs[0]
            p3.text = "POWERED BY RLAI DASH"
            p3.font.size = Pt(9)
            p3.font.color.rgb = RGBColor(0xBB, 0xBB, 0xBB)
            p3.alignment = PP_ALIGN.CENTER

        elif layout == "kpi" and s.get("kpis"):
            _add_header(slide, s, i, total)
            kpis = s["kpis"][:4]
            kpi_width = 11.0 / len(kpis)
            for ki, kpi in enumerate(kpis):
                left = 0.8 + ki * kpi_width
                # Value
                vb = slide.shapes.add_textbox(Inches(left), Inches(2.0), Inches(kpi_width - 0.3), Inches(1.0))
                vp = vb.text_frame.paragraphs[0]
                vp.text = str(kpi.get("value", ""))
                vp.font.size = Pt(36)
                vp.font.bold = True
                vp.alignment = PP_ALIGN.CENTER
                # Label
                lb = slide.shapes.add_textbox(Inches(left), Inches(3.0), Inches(kpi_width - 0.3), Inches(0.4))
                lp = lb.text_frame.paragraphs[0]
                lp.text = kpi.get("label", "").upper()
                lp.font.size = Pt(9)
                lp.font.color.rgb = RGBColor(0x88, 0x88, 0x88)
                lp.alignment = PP_ALIGN.CENTER
                # Change
                if kpi.get("change"):
                    cb = slide.shapes.add_textbox(Inches(left), Inches(3.4), Inches(kpi_width - 0.3), Inches(0.4))
                    cp = cb.text_frame.paragraphs[0]
                    cp.text = kpi["change"]
                    cp.font.size = Pt(14)
                    cp.font.bold = True
                    is_pos = kpi["change"].startswith("+") or "▲" in kpi["change"]
                    cp.font.color.rgb = RGBColor(0x00, 0x87, 0x3c) if is_pos else RGBColor(0xD3, 0x2F, 0x2F)
                    cp.alignment = PP_ALIGN.CENTER
            _add_bullets(slide, s.get("bullets"), top=4.2, height=1.8)
            _add_action_line(slide, s)

        elif layout in ("exhibit", "trend") or s.get("chart"):
            _add_header(slide, s, i, total)
            chart = s.get("chart", {})
            labels = chart.get("labels", [])
            values = chart.get("values", [])
            chart_type = chart.get("type", "bar")

            if labels and values:
                try:
                    from pptx.chart.data import CategoryChartData
                    from pptx.enum.chart import XL_CHART_TYPE

                    chart_data = CategoryChartData()
                    chart_data.categories = [str(l) for l in labels[:12]]
                    chart_data.add_series("Value", [float(v) if isinstance(v, (int, float)) else 0 for v in values[:12]])

                    if chart_type == "pie":
                        xl_type = XL_CHART_TYPE.PIE
                    elif chart_type == "line":
                        xl_type = XL_CHART_TYPE.LINE
                    elif chart_type == "horizontal_bar":
                        xl_type = XL_CHART_TYPE.BAR_CLUSTERED
                    else:
                        xl_type = XL_CHART_TYPE.COLUMN_CLUSTERED

                    chart_shape = slide.shapes.add_chart(
                        xl_type, Inches(0.5), Inches(1.8), Inches(7), Inches(4.2), chart_data
                    )
                    pptx_chart = chart_shape.chart
                    pptx_chart.has_legend = False
                    if chart.get("title"):
                        pptx_chart.has_title = True
                        pptx_chart.chart_title.text_frame.paragraphs[0].text = chart["title"]
                        pptx_chart.chart_title.text_frame.paragraphs[0].font.size = Pt(10)

                    # Style the chart
                    try:
                        series = pptx_chart.series[0]
                        series.format.fill.solid()
                        series.format.fill.fore_color.rgb = RGBColor(0xD2, 0x47, 0x26)
                    except Exception:
                        pass

                except Exception:
                    # Fallback: render as table if chart fails
                    cb2 = slide.shapes.add_textbox(Inches(0.5), Inches(1.7), Inches(7), Inches(0.3))
                    cp2 = cb2.text_frame.paragraphs[0]
                    cp2.text = (chart.get("title", "EXHIBIT")).upper()
                    cp2.font.size = Pt(9)
                    cp2.font.color.rgb = RGBColor(0x88, 0x88, 0x88)
                    num_rows = min(len(labels), 10) + 1
                    tbl = slide.shapes.add_table(num_rows, 2, Inches(0.5), Inches(2.1), Inches(6.5), Inches(0.35 * num_rows))
                    table = tbl.table
                    table.cell(0, 0).text = "Category"
                    table.cell(0, 1).text = "Value"
                    for ci2 in range(2):
                        for paragraph in table.cell(0, ci2).text_frame.paragraphs:
                            paragraph.font.size = Pt(9)
                            paragraph.font.bold = True
                    for ri2, (lbl, val) in enumerate(zip(labels[:10], values[:10])):
                        table.cell(ri2+1, 0).text = str(lbl)
                        table.cell(ri2+1, 1).text = f"{val:,.0f}" if isinstance(val, (int, float)) else str(val)

            # Takeaways on right
