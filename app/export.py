"""
Export API
==========

Generate PDF from chat content and PPTX from dashboards.
"""

import io
import re as _re
import tempfile
from pathlib import Path

from fastapi import APIRouter, HTTPException, Request
from fastapi.responses import StreamingResponse

router = APIRouter(prefix="/api/export", tags=["Export"])


def _get_user(request: Request) -> dict:
    user = getattr(getattr(request, 'state', None), 'user', None)
    if not user:
        raise HTTPException(401, "Not authenticated")
    return user


@router.post("/pdf")
async def export_pdf(request: Request):
    """Generate a PDF from markdown/HTML content."""
    _get_user(request)
    body = await request.json()
    content = body.get("content", "")
    title = _re.sub(r'[^\w\s\-.]', '', body.get("title", "Dash Export"))[:100] or 'export'

    if not content:
        raise HTTPException(400, "Content required")

    try:
        import markdown as md
        from weasyprint import HTML
    except ImportError:
        # Fallback: return HTML as downloadable file
        html_content = _build_html(title, content)
        buf = io.BytesIO(html_content.encode("utf-8"))
        return StreamingResponse(buf, media_type="text/html",
                                 headers={"Content-Disposition": f'attachment; filename="{title}.html"'})

    html_body = md.markdown(content, extensions=["tables", "fenced_code"])
    full_html = _build_html(title, html_body, is_raw_html=True)

    pdf_bytes = HTML(string=full_html).write_pdf()
    buf = io.BytesIO(pdf_bytes)
    return StreamingResponse(buf, media_type="application/pdf",
                             headers={"Content-Disposition": f'attachment; filename="{title}.pdf"'})


@router.post("/pptx")
async def export_pptx(request: Request):
    """Generate a PPTX from dashboard widgets or slides data."""
    _get_user(request)
    body = await request.json()
    title = _re.sub(r'[^\w\s\-.]', '', body.get("title", "Dash Report"))[:100] or 'export'
    slides = body.get("slides", [])

    if not slides:
        raise HTTPException(400, "Slides required")

    try:
        from pptx import Presentation
        from pptx.util import Inches, Pt
    except ImportError:
        raise HTTPException(500, "python-pptx not installed")

    prs = Presentation()
    prs.slide_width = Inches(13.333)
    prs.slide_height = Inches(7.5)

    # Title slide
    title_layout = prs.slide_layouts[0]
    slide = prs.slides.add_slide(title_layout)
    slide.shapes.title.text = title
    if slide.placeholders[1]:
        slide.placeholders[1].text = "Generated by Dash"

    # Content slides
    blank_layout = prs.slide_layouts[5]  # blank
    for s in slides:
        slide = prs.slides.add_slide(blank_layout)
        # Title
        from pptx.util import Emu
        txBox = slide.shapes.add_textbox(Inches(0.5), Inches(0.3), Inches(12), Inches(0.8))
        tf = txBox.text_frame
        p = tf.paragraphs[0]
        p.text = s.get("title", "")
        p.font.size = Pt(24)
        p.font.bold = True

        # Content
        content = s.get("content", "")
        if content:
            txBox2 = slide.shapes.add_textbox(Inches(0.5), Inches(1.3), Inches(12), Inches(5.5))
            tf2 = txBox2.text_frame
            tf2.word_wrap = True
            # Split content into lines
            for line in content.split("\n"):
                p2 = tf2.add_paragraph()
                p2.text = line
                p2.font.size = Pt(14)

        # Table data
        headers = s.get("headers", [])
        rows = s.get("rows", [])
        if headers and rows:
            num_cols = len(headers)
            num_rows = min(len(rows), 15) + 1  # header + data rows
            left = Inches(0.5)
            top = Inches(1.5) if not content else Inches(4.0)
            width = Inches(12)
            height = Inches(0.3 * num_rows)

            table_shape = slide.shapes.add_table(num_rows, num_cols, left, top, width, height)
            table = table_shape.table

            for ci, h in enumerate(headers):
                cell = table.cell(0, ci)
                cell.text = h
                for paragraph in cell.text_frame.paragraphs:
                    paragraph.font.size = Pt(10)
                    paragraph.font.bold = True

            for ri, row in enumerate(rows[:15]):
                for ci, val in enumerate(row):
                    if ci < num_cols:
                        cell = table.cell(ri + 1, ci)
                        cell.text = str(val)
                        for paragraph in cell.text_frame.paragraphs:
                            paragraph.font.size = Pt(9)

    buf = io.BytesIO()
    prs.save(buf)
    buf.seek(0)
    return StreamingResponse(buf, media_type="application/vnd.openxmlformats-officedocument.presentationml.presentation",
                             headers={"Content-Disposition": f'attachment; filename="{title}.pptx"'})


@router.post("/report-from-chat")
async def generate_report_from_chat(request: Request):
    """Generate a structured report from a chat session."""
    _get_user(request)
    body = await request.json()
    messages = body.get("messages", [])
    title = _re.sub(r'[^\w\s\-.]', '', body.get("title", "Analysis Report"))[:100] or 'export'

    if not messages:
        raise HTTPException(400, "Messages required")

    # Build conversation text
    convo = "\n\n".join(f"{'Q' if m.get('role') == 'user' else 'A'}: {m.get('content', '')}" for m in messages)

    from os import getenv
    import httpx
    api_key = getenv("OPENROUTER_API_KEY", "")

    report_content = f"# {title}\n\n"
    if api_key:
        try:
            prompt = f"""Convert this data analysis conversation into a structured report.

CONVERSATION:
{convo[:3000]}

Generate a professional report with these sections:
1. Executive Summary (2-3 sentences)
2. Key Findings (bullet points)
3. Data Analysis (details with any tables/numbers mentioned)
4. Recommendations (if applicable)

Write in markdown format."""

            resp = httpx.post(
                "https://openrouter.ai/api/v1/chat/completions",
                headers={"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"},
                json={"model": "openai/gpt-5.4-mini", "messages": [{"role": "user", "content": prompt}], "max_tokens": 2000, "temperature": 0.2},
                timeout=20,
            )
            result = resp.json()
            report_content = result.get("choices", [{}])[0].get("message", {}).get("content", report_content)
        except Exception:
            # Fallback: just concatenate the conversation
            report_content += convo
    else:
        report_content += convo

    try:
        import markdown as md
        from weasyprint import HTML
        html_body = md.markdown(report_content, extensions=["tables", "fenced_code"])
        full_html = _build_html(title, html_body, is_raw_html=True)
        pdf_bytes = HTML(string=full_html).write_pdf()
        buf = io.BytesIO(pdf_bytes)
        return StreamingResponse(buf, media_type="application/pdf",
                                 headers={"Content-Disposition": f'attachment; filename="{title}.pdf"'})
    except ImportError:
        html_content = _build_html(title, report_content)
        buf = io.BytesIO(html_content.encode("utf-8"))
        return StreamingResponse(buf, media_type="text/html",
                                 headers={"Content-Disposition": f'attachment; filename="{title}.html"'})


def _build_html(title: str, body: str, is_raw_html: bool = False) -> str:
    """Build a full HTML document with print-friendly styling."""
    import markdown as md

    if not is_raw_html:
        body = md.markdown(body, extensions=["tables", "fenced_code"])

    return f"""<!DOCTYPE html>
<html>
<head>
<meta charset="utf-8">
<title>{title}</title>
<style>
  body {{ font-family: 'Helvetica Neue', Arial, sans-serif; max-width: 800px; margin: 40px auto; padding: 0 20px; color: #333; font-size: 14px; line-height: 1.6; }}
  h1 {{ font-size: 24px; border-bottom: 2px solid #333; padding-bottom: 8px; }}
  h2 {{ font-size: 18px; margin-top: 24px; }}
  h3 {{ font-size: 15px; }}
  table {{ width: 100%; border-collapse: collapse; margin: 12px 0; font-size: 12px; }}
  th {{ background: #333; color: #fff; padding: 6px 10px; text-align: left; font-weight: 700; }}
  td {{ padding: 5px 10px; border: 1px solid #ddd; }}
  tr:nth-child(even) td {{ background: #f9f9f9; }}
  code {{ background: #f0f0f0; padding: 1px 4px; font-size: 12px; }}
  pre {{ background: #333; color: #0f0; padding: 12px; overflow-x: auto; font-size: 12px; }}
  pre code {{ background: none; color: inherit; }}
  .header {{ text-align: center; margin-bottom: 30px; }}
  .footer {{ margin-top: 40px; font-size: 10px; color: #999; text-align: center; border-top: 1px solid #ddd; padding-top: 10px; }}
</style>
</head>
<body>
<div class="header"><h1>{title}</h1></div>
{body}
<div class="footer">Generated by Dash Data Agent</div>
</body>
</html>"""
